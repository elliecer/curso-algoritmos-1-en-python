# Definir algunas variables para trabajar
a = 20
b = 20

# Suma (+)
suma = a + b
print(f"Suma: {a} + {b} = {suma}")

# Resta (-)
resta = a - b
print(f"Resta: {a} - {b} = {resta}")

# Multiplicación (*)
multiplicacion = a * b
print(f"Multiplicación: {a} * {b} = {multiplicacion}")

# División (/)
division = a / b
print(f"División: {a} / {b} = {division}")

# División entera (//)
division_entera = a // b
print(f"División entera: {a} // {b} = {division_entera}")

# Módulo o resto (%)
modulo = a % b
print(f"Módulo: {a} % {b} = {modulo}")

# Exponente (**)
exponente = a ** b
print(f"Exponente: {a} ** {b} = {exponente}")

# Operaciones combinadas
resultado = a + b * 2 - (a / b)
print(f"{a} + {b} * 2 - ({a} / {b}) = {resultado}")